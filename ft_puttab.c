#include "libft.h"

size_t		ft_puttab(char **tab)
{
	int		i;
	size_t	res;

	i = 0;
	res = 0;
	while (tab[i] != '\0')
	{
		res += ft_putendl(tab[i]);
		i++;
	}
	return (res);
}
